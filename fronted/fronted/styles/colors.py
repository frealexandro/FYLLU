from enum import Enum


class Color(Enum):
    PRIMARY = "#000000" #negro
    SECONDARY = "#F0A14A" #naranja
    BACKGROUND = "#FFFFFF" #blanco
    CONTENT = "#F0A14A", #naranja
    PURPLE = "#9146ff" #morado


class TextColor(Enum):
    HEADER = "#F0A14A" #naranja
    BODY = "#C3C7CB" #gris
    FOOTER = "#A3ABB2" #gris